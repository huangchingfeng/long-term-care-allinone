HEAD_FILL = PatternFill("solid", fgColor="0B5D52")
HEAD_FONT = Font(bold=True, color="FFFFFF", size=11, name="Noto Sans CJK TC")
TITLE_FONT = Font(bold=True, color="0B5D52", size=11, name="Noto Sans CJK TC")
CELL_FONT = Font(size=11, name="Noto Sans CJK TC")
THIN = Side(style="thin", color="C7E9DF")
BORDER = Border(left=THIN, right=THIN, top=THIN, bottom=THIN)


def build(filename, sheet_name, headers, rows, widths=None):
    wb = openpyxl.Workbook()
    ws = wb.active
    ws.title = sheet_name
    n = len(headers)
    # 第1列：免責聲明（跨欄）
    ws.merge_cells(start_row=1, start_column=1, end_row=1, end_column=n)
    c = ws.cell(row=1, column=1, value=DISCLAIMER)
    c.font = TITLE_FONT
    c.alignment = Alignment(horizontal="left", vertical="center", wrap_text=False)
    ws.row_dimensions[1].height = 22
    # 第2列：表頭
    for j, h in enumerate(headers, 1):
        cell = ws.cell(row=2, column=j, value=h)
        cell.font = HEAD_FONT
        cell.fill = HEAD_FILL
        cell.alignment = Alignment(horizontal="center", vertical="center")
        cell.border = BORDER
    # 資料列
    for i, row in enumerate(rows, 3):
        for j, val in enumerate(row, 1):
            cell = ws.cell(row=i, column=j, value=val)
            cell.font = CELL_FONT
            cell.alignment = Alignment(vertical="center")
            cell.border = BORDER
    # 欄寬
    for j in range(1, n + 1):
        col = openpyxl.utils.get_column_letter(j)
        ws.column_dimensions[col].width = (widths[j - 1] if widths else 16)
    ws.freeze_panes = "A3"
    path = f"{OUT}/{filename}"
    wb.save(path)
    print(f"✅ {filename}  ({len(rows)} 列)")


